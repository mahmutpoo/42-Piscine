#include <unistd.h>
#include <stdio.h>
int ft_sqrt(int nb)
{
  if(nb <0)
  return 0;
  int i=1;

  while (nb >= i *i)
  {
     if(nb == (i*i))
     return i;
    i++;
  }
  return(0);
}
