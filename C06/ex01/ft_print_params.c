int		main(int argc, char *argv[])
{
	int i=1;
  int j=0;
  while(argc >i)
  {
    while(argv[i][j])
    {
      write(1,&argv[i][j],1);
      j++;
    }
    write(1,"\n",1);
    j=0;
    i++;
  }

  return 0;
}
